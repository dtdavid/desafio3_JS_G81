/* Ejercicio 1. Transforma esta declaración de función a una función de expresión. El ejercicio debe
quedar en un archivo nombrado `1_funcion.js`*/

// declaración de función
function example(a, b, c) {
    return a + b + c
}
//expresión de función
let suma = function (a, b, c) {
    return a + b + c
}
// se le asigna a una variable una función anónima y hará exactamente lo mismo que la función anterior

